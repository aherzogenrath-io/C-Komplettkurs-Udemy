#include <stdio.h>

int main()
{
    float float_value = 1750.30F;
    double double_value = 1750.30;
    int int_value = -1750;
    unsigned int uint_value = 17U;

    printf("float: %f\n double: %lf\n integer: %d\n unsigned integer: %u\n", float_value, double_value, int_value, uint_value);


    return 0;
}
